#     *Soma de Inteiros: Leia dois números inteiros e exiba a soma.

#     *Subtração e Diferença: Leia dois números e exiba o resultado do primeiro menos o segundo.

#     *Produto Simples: Receba dois números reais (double) e exiba a multiplicação entre eles com duas casas decimais.

#     *Divisão Real: Peça dois números e exiba o quociente real (ex: $5 / 2 = 2.5$).

#     *Divisão Inteira: Peça dois números inteiros e exiba apenas o quociente inteiro (ex: $5 / 2 = 2$).

#     *Resto da Divisão: Receba um número e exiba o resto da divisão dele por 2 (útil para verificar paridade).

#     *Cálculo de Potência: Sem usar a biblioteca cmath, peça um número e exiba ele elevado ao quadrado ($n \times n$).

#     *Média Simples: Receba três notas de um aluno e calcule a média aritmética.

#     *Conversão de Horas: Receba um valor em horas e converta para minutos e segundos.

#     Antecessor e Sucessor: Leia um número $n$ e imprima $n-1$ e $n+1$.
